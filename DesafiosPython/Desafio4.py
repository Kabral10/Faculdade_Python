# Criar um programa que leia algo pelo teclado e retorne seu tipo primitivo e todas informações possiveis sobre o ele

a = input('Digite algo: ')
print(type(a))
print(a.istitle())
print(a.isspace())
print(a.isalnum())
print(a.isspace())
print(a.isalpha())
print(a.isascii())
print(a.isdecimal())
print(a.isdigit())
print(a.isupper())
print(a.isidentifier())